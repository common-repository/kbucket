	<h1 style="margin-top:40px">Welcome to KBucket</h1>
	<p>This page has important links and information to help you get started with KBucket.</p>

	<h1 style="margin-top:10px">Getting Started</h1>
	<p>Now that you have installed KBucket you need to register and get your API key from our site <a target="_blank" href="https://optimalaccess.com/register">here</a>.</p>
	<p>Once you register, copy the API key from your profile page and enter it under the Settings-tab above.</p>
	<p>To populate your content hub, you need to install our <a target="_blank" href="https://optimalaccess.com/kurator">Kurator</a> chrome extension.</p>
	<p>Kurator will let you curate, organize and publish your information to your KBucket page.</p>

	<h1 style="margin-top:10px">Important Notes</h1>
	<p>In order to export files to KBucket, you will need to upgrade Kurator.</p>
    <p>We have 3 sets of Forums with important lessons and guidelines. You can ask questions and make your comments in any of these <a href="https://optimalaccess.com/forums/">forums</a>.</p>
